import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const GENERATED_HEADER = "/* This file is generated by scripts/generate-component-registry.mjs. Do not edit manually. */";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(scriptDir, "..");
const packageJsonPath = path.join(packageRoot, "package.json");
const componentsRoot = path.join(packageRoot, "src", "components");
const registryPath = path.join(packageRoot, "src", "component-registry.ts");
const versionPath = path.join(packageRoot, "src", "version.ts");
const globalTypesPath = path.join(packageRoot, "types", "components.d.ts");
const checkMode = process.argv.includes("--check");

const toPosixPath = (value) => value.split(path.sep).join("/");

const toPublicComponentName = (stem) => {
  if (!stem.startsWith("pu") || stem.length <= 2) {
    throw new Error(`Component file "${stem}.vue" must start with "pu".`);
  }

  return `P${stem.slice(1)}`;
};

const readExisting = async (filePath) => {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (error && error.code === "ENOENT") {
      return "";
    }
    throw error;
  }
};

const writeOrCheck = async (filePath, content) => {
  const existing = await readExisting(filePath);

  if (existing === content) {
    return false;
  }

  if (checkMode) {
    throw new Error(`Generated file is stale: ${path.relative(packageRoot, filePath)}`);
  }

  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
  return true;
};

const discoverComponents = async () => {
  const entries = await readdir(componentsRoot, { withFileTypes: true });
  const components = [];
  const seen = new Map();

  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const folder = entry.name;
    const folderPath = path.join(componentsRoot, folder);
    const files = await readdir(folderPath, { withFileTypes: true });

    for (const file of files) {
      if (!file.isFile() || !file.name.endsWith(".vue")) {
        continue;
      }

      const stem = file.name.slice(0, -".vue".length);
      const componentName = toPublicComponentName(stem);
      const componentPath = `./${toPosixPath(path.relative(path.join(packageRoot, "src"), path.join(folderPath, file.name)))}`;

      if (seen.has(componentName)) {
        throw new Error(
          `Duplicate component name "${componentName}" from ${seen.get(componentName)} and ${componentPath}.`,
        );
      }

      seen.set(componentName, componentPath);
      components.push({
        name: componentName,
        path: componentPath,
        typePath: componentPath.slice(0, -".vue".length),
      });
    }
  }

  return components.sort((a, b) => a.name.localeCompare(b.name));
};

const createRegistrySource = (components) => {
  const imports = components
    .map((component) => `import ${component.name} from '${component.path}'`)
    .join("\n");
  const typeExports = components
    .map((component) => `export type * from '${component.typePath}'`)
    .join("\n");
  const globalTypeEntries = components
    .map((component) => `    ${component.name}: typeof ${component.name}`)
    .join("\n");
  const exportNames = components.map((component) => `  ${component.name}`).join(",\n");
  const registryItems = components
    .map((component) => `  ['${component.name}', ${component.name}]`)
    .join(",\n");

  return `${GENERATED_HEADER}

import type { Component } from 'vue'

${imports}

export {
${exportNames}
}

${typeExports}

declare module 'vue' {
  export interface GlobalComponents {
${globalTypeEntries}
  }
}

type ComponentEntry = readonly [name: string, component: Component]

const components: readonly ComponentEntry[] = [
${registryItems}
]

export default components
`;
};

const createGlobalTypesSource = (components, packageName) => {
  const entries = components
    .map((component) => `    ${component.name}: typeof import('${packageName}')['${component.name}']`)
    .join("\n");

  return `${GENERATED_HEADER}

declare module 'vue' {
  export interface GlobalComponents {
${entries}
  }
}

export {}
`;
};

const createVersionSource = (version) => `${GENERATED_HEADER}

export const version = '${version}'
`;

const packageJson = JSON.parse(await readFile(packageJsonPath, "utf8"));
const components = await discoverComponents();
const registryChanged = await writeOrCheck(registryPath, createRegistrySource(components));
const versionChanged = await writeOrCheck(versionPath, createVersionSource(packageJson.version));
const globalTypesChanged = await writeOrCheck(globalTypesPath, createGlobalTypesSource(components, packageJson.name));

if (!checkMode) {
  const changed = [];
  if (registryChanged) changed.push(path.relative(packageRoot, registryPath));
  if (versionChanged) changed.push(path.relative(packageRoot, versionPath));
  if (globalTypesChanged) changed.push(path.relative(packageRoot, globalTypesPath));
  if (changed.length > 0) {
    console.log(`Generated ${changed.join(", ")}`);
  } else {
    console.log("Generated files are already up to date.");
  }
}
