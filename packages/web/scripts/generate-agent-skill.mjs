const renderFiles = (model) => {
  const files = new Map();
  files.set("SKILL.md", renderSkill(model));
  files.set("references/component-map.md", renderComponentMap(model));
  files.set("references/composition-recipes.md", renderCompositionRecipes(model));
  files.set("references/usage-rules.md", renderUsageRules(model));

  for (const reference of model.skillReferences) {
    files.set(reference.target, `${GENERATED_HEADER}\n${reference.content}\n`);
  }

  for (const component of model.components) {
    files.set(componentFileName(component.name), renderComponentReference(model, component));
  }

  return files;
};

const compareOrWrite = async (files) => {
  if (checkMode) {
    const mismatches = [];
    for (const [relativePath, content] of files) {
      const filePath = path.join(outputRoot, relativePath);
      const existing = await readMaybe(filePath);
      if (existing !== content) {
        mismatches.push(relativePath);
      }
    }
    if (mismatches.length > 0) {
      throw new Error(`Generated skill files are stale: ${mismatches.join(", ")}`);
    }
    return;
  }

  if (!path.relative(packageRoot, outputRoot).startsWith("skills")) {
    throw new Error(`Refusing to remove unexpected output path: ${outputRoot}`);
  }

  await rm(outputRoot, { recursive: true, force: true });
  for (const [relativePath, content] of files) {
    const filePath = path.join(outputRoot, relativePath);
    await mkdir(path.dirname(filePath), { recursive: true });
    await writeFile(filePath, content, "utf8");
  }
};

const model = await buildModel();
const files = renderFiles(model);
await compareOrWrite(files);

for (const warning of model.warnings) {
  console.warn(`Warning: ${warning}`);
}

if (checkMode) {
  console.log(`Agent skill files are up to date (${files.size} files).`);
} else {
  console.log(`Generated agent skill files (${files.size} files).`);
}
