const readMaybe = async (filePath) => {
  try {
    return await readText(filePath);
  } catch (error) {
    if (error && error.code === "ENOENT") {
      return "";
    }
    throw error;
  }
};

const walk = async (dir) => {
  let entries;
  try {
    entries = await readdir(dir, { withFileTypes: true });
  } catch (error) {
    if (error && error.code === "ENOENT") {
      return [];
    }
    throw error;
  }

  const files = [];
  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walk(entryPath)));
    } else if (entry.isFile()) {
      files.push(entryPath);
    }
  }
  return files;
};

const findMatchingBrace = (source, openIndex) => {
  let depth = 0;
  let quote = "";
  let escaped = false;
  let lineComment = false;
  let blockComment = false;

  for (let index = openIndex; index < source.length; index += 1) {
    const char = source[index];
    const next = source[index + 1];

    if (lineComment) {
      if (char === "\n") lineComment = false;
      continue;
    }

    if (blockComment) {
      if (char === "*" && next === "/") {
        blockComment = false;
        index += 1;
      }
      continue;
    }

    if (quote) {
      if (escaped) {
        escaped = false;
      } else if (char === "\\") {
        escaped = true;
      } else if (char === quote) {
        quote = "";
      }
      continue;
    }

    if (char === "/" && next === "/") {
      lineComment = true;
      index += 1;
      continue;
    }

    if (char === "/" && next === "*") {
      blockComment = true;
      index += 1;
      continue;
    }

    if (char === "'" || char === '"' || char === "`") {
      quote = char;
      continue;
    }

    if (char === "{") {
      depth += 1;
    } else if (char === "}") {
      depth -= 1;
      if (depth === 0) {
        return index;
      }
    }
  }

  return -1;
};

const extractExportedObject = (source, constName) => {
  const pattern = new RegExp(`export\\s+const\\s+${constName}\\s*=`);
  const match = pattern.exec(source);
  if (!match) {
    return "";
  }

  const openIndex = source.indexOf("{", match.index);
  if (openIndex === -1) {
    return "";
  }

  const closeIndex = findMatchingBrace(source, openIndex);
  if (closeIndex === -1) {
    return "";
  }

  return source.slice(openIndex + 1, closeIndex);
};

const skipWhitespaceAndComments = (source, index) => {
  let cursor = index;
  while (cursor < source.length) {
    if (/\s/.test(source[cursor]) || source[cursor] === ",") {
      cursor += 1;
      continue;
    }
    if (source[cursor] === "/" && source[cursor + 1] === "/") {
      cursor = source.indexOf("\n", cursor);
      if (cursor === -1) return source.length;
      continue;
    }
    if (source[cursor] === "/" && source[cursor + 1] === "*") {
      const end = source.indexOf("*/", cursor + 2);
      cursor = end === -1 ? source.length : end + 2;
      continue;
    }
    break;
  }
  return cursor;
};

const readQuoted = (source, index) => {
  const quote = source[index];
  let cursor = index + 1;
  let value = "";
  let escaped = false;
  while (cursor < source.length) {
    const char = source[cursor];
    if (escaped) {
      value += char;
      escaped = false;
    } else if (char === "\\") {
      escaped = true;
    } else if (char === quote) {
      return { value, end: cursor + 1 };
    } else {
      value += char;
    }
    cursor += 1;
  }
  return { value, end: cursor };
};

const parseTopLevelKeys = (objectBody) => {
  const keys = [];
  let index = 0;
  let depth = 0;
  let quote = "";
  let escaped = false;
  let lineComment = false;
  let blockComment = false;

  while (index < objectBody.length) {
    const char = objectBody[index];
    const next = objectBody[index + 1];

    if (lineComment) {
      if (char === "\n") lineComment = false;
      index += 1;
      continue;
    }

    if (blockComment) {
      if (char === "*" && next === "/") {
        blockComment = false;
        index += 2;
      } else {
        index += 1;
      }
      continue;
    }

    if (quote) {
      if (escaped) {
        escaped = false;
      } else if (char === "\\") {
        escaped = true;
      } else if (char === quote) {
        quote = "";
      }
      index += 1;
      continue;
    }

    if (char === "/" && next === "/") {
      lineComment = true;
      index += 2;
      continue;
    }

    if (char === "/" && next === "*") {
      blockComment = true;
      index += 2;
      continue;
    }

    if (char === "'" || char === '"' || char === "`") {
      quote = char;
      index += 1;
      continue;
    }

    if (char === "{" || char === "[" || char === "(") {
      depth += 1;
      index += 1;
      continue;
    }

    if (char === "}" || char === "]" || char === ")") {
      depth = Math.max(0, depth - 1);
      index += 1;
      continue;
    }

    if (depth !== 0) {
      index += 1;
      continue;
    }

    index = skipWhitespaceAndComments(objectBody, index);
    if (index >= objectBody.length) break;

    if (objectBody.startsWith("...", index)) {
      const spread = /^\.\.\.([A-Za-z_$][\w$]*)/.exec(objectBody.slice(index));
      if (spread) {
        keys.push(`...${spread[1]}`);
        index += spread[0].length;
      } else {
        index += 3;
      }
      continue;
    }

    let key = "";
    if (objectBody[index] === "'" || objectBody[index] === '"') {
      const quoted = readQuoted(objectBody, index);
      key = quoted.value;
      index = quoted.end;
    } else {
      const identifier = /^[A-Za-z_$][\w$-]*/.exec(objectBody.slice(index));
      if (!identifier) {
        index += 1;
        continue;
      }
      key = identifier[0];
      index += identifier[0].length;
    }

    index = skipWhitespaceAndComments(objectBody, index);
    if (objectBody[index] === ":") {
      keys.push(key);
    }
  }

  return [...new Set(keys)];
};

const extractTypes = (source) => {
  const names = [];
  for (const match of source.matchAll(/^export\s+type\s+([A-Z][A-Za-z0-9_]+)/gm)) {
    names.push(match[1]);
  }
  for (const match of source.matchAll(/^export\s+interface\s+([A-Z][A-Za-z0-9_]+)/gm)) {
    names.push(match[1]);
  }
  return [...new Set(names)].sort();
};

const assertRelativeInsideRepo = (relativePath, label) => {
  if (!relativePath || path.isAbsolute(relativePath)) {
    throw new Error(`${label} must be a relative path.`);
  }

  const resolvedPath = path.resolve(packageRoot, relativePath);
  const relativeFromRoot = path.relative(repoRoot, resolvedPath);
  if (relativeFromRoot.startsWith("..") || path.isAbsolute(relativeFromRoot)) {
    throw new Error(`${label} escapes the repository root: ${relativePath}`);
  }

  return resolvedPath;
};

const assertSkillReferenceTarget = (target) => {
  if (!target || path.isAbsolute(target)) {
    throw new Error(`Skill reference target must be a relative path.`);
  }

  const normalized = toPosixPath(path.normalize(target));
  if (normalized.startsWith("../") || normalized.includes("/../") || !normalized.startsWith("references/")) {
    throw new Error(`Skill reference target must stay under references/: ${target}`);
  }

  return normalized;
};

const extractSkillExcerpt = (source, sourcePath) => {
  const startIndex = source.indexOf(SKILL_EXCERPT_START);
  const endIndex = source.indexOf(SKILL_EXCERPT_END);
  if (startIndex === -1 || endIndex === -1 || endIndex <= startIndex) {
    throw new Error(
      `Skill reference source must include ${SKILL_EXCERPT_START} and ${SKILL_EXCERPT_END}: ${sourcePath}`,
    );
  }

  return source.slice(startIndex + SKILL_EXCERPT_START.length, endIndex).trim();
};

const loadSkillReferences = async (seed) => {
  const references = [];
  for (const entry of seed.skillReferences || []) {
    const sourcePath = assertRelativeInsideRepo(entry.source, "Skill reference source");
    const target = assertSkillReferenceTarget(entry.target);
    const source = await readText(sourcePath);
    references.push({
      source: entry.source,
      target,
      description: entry.description || "durable package guidance.",
      content: extractSkillExcerpt(source, entry.source),
    });
  }
  return references;
};

const extractSlots = (source) => {
  const slots = new Set();
  for (const match of source.matchAll(/<slot\b([^>]*)>/g)) {
    const attrs = match[1] || "";
    const nameMatch = /\bname=["']([^"']+)["']/.exec(attrs);
    slots.add(nameMatch ? nameMatch[1] : "default");
  }
  return [...slots].sort((a, b) => (a === "default" ? -1 : b === "default" ? 1 : a.localeCompare(b)));
};

const discoverRegistryComponents = async () => {
  const source = await readText(registryPath);
  return [...source.matchAll(/^import\s+(Pu[A-Za-z0-9_]+)\s+from\s+'([^']+)'/gm)]
    .map((match) => ({
      name: match[1],
      vueImportPath: match[2],
      vuePath: path.join(packageRoot, "src", match[2].replace(/^\.\//, "")),
    }))
    .sort((a, b) => a.name.localeCompare(b.name));
};

const discoverStories = async () => {
  const files = await walk(storiesRoot);
  const byComponent = new Map();

  for (const filePath of files.filter((file) => file.endsWith(".story.vue"))) {
    const source = await readText(filePath);
    const basename = path.basename(filePath, ".story.vue");
    const relativePath = toPosixPath(path.relative(packageRoot, filePath));
    const storyMatch = /<Story\b[^>]*\btitle=["']([^"']+)["'][^>]*\bgroup=["']([^"']+)["']/s.exec(source);
    const group = storyMatch ? storyMatch[2] : path.basename(path.dirname(filePath));
    const variants = [...source.matchAll(/<Variant\b[^>]*\btitle=["']([^"']+)["']/g)].map((match) => match[1]);
    const covered = new Set([basename]);

    for (const match of source.matchAll(/@pu-story-covers\s+([A-Za-z0-9_\s]+)/g)) {
      for (const name of match[1].trim().split(/\s+/)) {
        if (name) covered.add(name);
      }
    }

    for (const componentName of covered) {
      byComponent.set(componentName, {
        file: relativePath,
        group,
        variants,
      });
    }
  }

  return byComponent;
};

const getComponentSource = async (component) => {
  const vueSource = await readMaybe(component.vuePath);
  const stem = path.basename(component.vuePath, ".vue");
  const tsPath = path.join(path.dirname(component.vuePath), `${stem}.ts`);
  const tsSource = await readMaybe(tsPath);
  return { vueSource, tsSource, tsPath };
};

const buildModel = async () => {
  const [packageJson, seed, registryComponents, stories] = await Promise.all([
    readJson(packageJsonPath),
    readJson(seedPath),
    discoverRegistryComponents(),
    discoverStories(),
  ]);

  const warnings = [];
  const errors = [];
  const seedComponents = seed.components || {};
  const categoryNames = new Set(Object.keys(seed.storyGroups || {}));
  const registryNames = new Set(registryComponents.map((component) => component.name));

  for (const name of Object.keys(seedComponents)) {
    if (!registryNames.has(name) && !seedComponents[name].ignore) {
      warnings.push(`Seed entry "${name}" does not match a public registry component.`);
    }
  }

  const components = [];
  for (const component of registryComponents) {
    const entry = seedComponents[component.name];
    if (!entry) {
      errors.push(`Missing seed entry for public component ${component.name}.`);
      continue;
    }
    if (entry.ignore) {
      if (!entry.reason) {
        errors.push(`Ignored component ${component.name} must include a reason.`);
      }
      continue;
    }
    if (!categoryNames.has(entry.category)) {
      errors.push(`Component ${component.name} uses unknown category "${entry.category}".`);
    }
    if (!categoryNames.has(entry.storyGroup)) {
      errors.push(`Component ${component.name} uses unknown storyGroup "${entry.storyGroup}".`);
    }

    const story = stories.get(component.name);
    if (!story) {
      warnings.push(`Component ${component.name} has no matching story coverage.`);
    } else if (entry.storyGroup && story.group !== entry.storyGroup) {
      warnings.push(`Component ${component.name} story group "${story.group}" differs from seed storyGroup "${entry.storyGroup}".`);
    }
    if (entry.storyFile && story && entry.storyFile !== story.file) {
      warnings.push(`Component ${component.name} seed storyFile "${entry.storyFile}" differs from discovered story "${story.file}".`);
    }

    const { vueSource, tsSource, tsPath } = await getComponentSource(component);
    const stem = path.basename(component.vuePath, ".vue");
    const propsName = `${stem}Props`;
    const emitsName = `${stem}Emits`;
    const props = parseTopLevelKeys(extractExportedObject(tsSource, propsName));
    const events = parseTopLevelKeys(extractExportedObject(tsSource, emitsName));

    if (tsSource && props.length === 0) {
      warnings.push(`Component ${component.name} has no extracted props from ${toPosixPath(path.relative(packageRoot, tsPath))}.`);
    }

    for (const target of entry.composeWith || []) {
      if (!registryNames.has(target)) {
        errors.push(`Component ${component.name} composeWith target ${target} is not public.`);
      }
    }

    components.push({
      name: component.name,
      category: entry.category,
      storyGroup: entry.storyGroup,
      intent: entry.intent || [],
      preferWhen: entry.preferWhen || [],
      avoidWhen: entry.avoidWhen || [],
      composeWith: entry.composeWith || [],
      apiCaveats: entry.apiCaveats || [],
      accessibilityNotes: entry.accessibilityNotes || [],
      commonMistakes: entry.commonMistakes || [],
      sourceFiles: [
        toPosixPath(path.relative(packageRoot, component.vuePath)),
        tsSource ? toPosixPath(path.relative(packageRoot, tsPath)) : "",
      ].filter(Boolean),
      importName: component.name,
      props,
      slots: extractSlots(vueSource),
      events,
      exportedTypes: extractTypes(tsSource),
      story: story || (entry.storyFile ? { file: entry.storyFile, group: entry.storyGroup, variants: [] } : null),
    });
  }

  if (errors.length > 0) {
    throw new Error(errors.join("\n"));
  }

  return {
    packageName: packageJson.name,
    packageVersion: packageJson.version,
    skill: seed.skill,
    storyGroups: seed.storyGroups,
    skillReferences: await loadSkillReferences(seed),
    components: components.sort((a, b) => a.name.localeCompare(b.name)),
    warnings,
  };
};

